class ExhausterViewSet(viewsets.ModelViewSet):
    """
    ViewSet for Viewing and Editing the Exhauster Registry.
    """
    queryset = Exhauster.objects.all().order_by('-date_registered', 'reg_no')
    serializer_class = ExhausterSerializer
    permission_classes = [IsAuthenticated]

    def get_queryset(self):
        """Filter exhausters based on query parameters"""
        queryset = super().get_queryset()
        
        # Filter by status
        status = self.request.query_params.get('status', None)
        if status:
            queryset = queryset.filter(status=status)
        
        # Filter by registration number (partial match)
        reg_no = self.request.query_params.get('reg_no', None)
        if reg_no:
            queryset = queryset.filter(reg_no__icontains=reg_no)
        
        # Filter by owner (partial match)
        owner = self.request.query_params.get('owner', None)
        if owner:
            queryset = queryset.filter(owner__icontains=owner)
            
        return queryset

    @action(detail=False, methods=['get'])
    def active(self, request):
        """Get only active exhausters"""
        active_exhausters = self.get_queryset().filter(status='active')
        serializer = self.get_serializer(active_exhausters, many=True)
        return Response(serializer.data)

    @action(detail=True, methods=['get'])
    def licenses(self, request, pk=None):
        """Get all licenses for a specific exhauster"""
        exhauster = self.get_object()
        licenses = exhauster.licenses.all().order_by('-end_date')
        serializer = LicenseSerializer(licenses, many=True)
        return Response(serializer.data)

    @action(detail=True, methods=['get'])
    def collections(self, request, pk=None):
        """Get all sludge collections for a specific exhauster"""
        exhauster = self.get_object()
        collections = exhauster.collections.all().order_by('-collection_date')
        serializer = SludgeCollectionSerializer(collections, many=True)
        return Response(serializer.data)

    @action(detail=False, methods=['get'])
    def summary(self, request):
        """Get summary statistics for exhausters"""
        queryset = self.get_queryset()
        total = queryset.count()
        active = queryset.filter(status='active').count()
        inactive = queryset.filter(status='inactive').count()
        suspended = queryset.filter(status='suspended').count()
        
        return Response({
            'total': total,
            'active': active,
            'inactive': inactive,
            'suspended': suspended,
            'active_percentage': round((active / total * 100) if total > 0 else 0, 2)
        })


