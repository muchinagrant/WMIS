class TreatmentLogViewSet(viewsets.ModelViewSet):
    """
    ViewSet for Viewing and Editing Treatment Logs.
    """
    queryset = TreatmentLog.objects.all().order_by('-report_date', '-created_at')
    serializer_class = TreatmentLogSerializer
    permission_classes = [IsAuthenticated]

    def get_queryset(self):
        """Filter treatment logs based on user role and query params"""
        queryset = super().get_queryset()
        user = self.request.user
        
        if user.role == 'operator':
            queryset = queryset.filter(operator=user)
        
        start_date = self.request.query_params.get('start_date', None)
        end_date = self.request.query_params.get('end_date', None)
        
        if start_date:
            queryset = queryset.filter(report_date__gte=start_date)
        if end_date:
            queryset = queryset.filter(report_date__lte=end_date)
        
        alert_only = self.request.query_params.get('alert_only', None)
        if alert_only and alert_only.lower() == 'true':
            queryset = queryset.filter(alert=True)
            
        return queryset

    def perform_create(self, serializer):
        serializer.save(operator=self.request.user)
    
    @action(detail=True, methods=['get'])
    def parameters(self, request, pk=None):
        """Get all parameters for a specific treatment log"""
        tlog = self.get_object()
        parameters = tlog.parameters.all().order_by('parameter')
        from .serializers import TreatmentParameterSerializer
        serializer = TreatmentParameterSerializer(parameters, many=True)
        return Response(serializer.data)
    
    @action(detail=True, methods=['post'])
    def add_parameter(self, request, pk=None):
        """Add a new parameter to an existing treatment log"""
        tlog = self.get_object()
        from .serializers import TreatmentParameterSerializer
        
        serializer = TreatmentParameterSerializer(data=request.data)
        if serializer.is_valid():
            serializer.save(tlog=tlog)
            self._update_alert_status(tlog)
            return Response(serializer.data, status=status.HTTP_201_CREATED)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)
    
    @action(detail=False, methods=['get'])
    def alerts(self, request):
        """Get only treatment logs with active alerts"""
        alerts = self.get_queryset().filter(alert=True)
        serializer = self.get_serializer(alerts, many=True)
        return Response(serializer.data)
    
    @action(detail=False, methods=['get'])
    def my_logs(self, request):
        """Get treatment logs created by the current user"""
        logs = self.get_queryset().filter(operator=request.user)
        serializer = self.get_serializer(logs, many=True)
        return Response(serializer.data)
    
    @action(detail=False, methods=['get'])
    def summary(self, request):
        """Get summary statistics for treatment logs"""
        queryset = self.get_queryset()
        total = queryset.count()
        with_alerts = queryset.filter(alert=True).count()
        
        thirty_days_ago = timezone.now().date() - datetime.timedelta(days=30)
        recent_logs = queryset.filter(report_date__gte=thirty_days_ago)
        
        return Response({
            'total_logs': total,
            'logs_with_alerts': with_alerts,
            'alert_percentage': round((with_alerts / total * 100) if total > 0 else 0, 2),
            'recent_logs_count': recent_logs.count(),
        })
    
    def _update_alert_status(self, tlog):
        """Helper method to recalculate alert status for a treatment log"""
        has_alert = False
        for param in tlog.parameters.all():
            param_name = param.parameter.lower()
            effluent = param.effluent_value
            
            if effluent is None:
                continue
                
            if param_name == 'ph' and (effluent < 6.5 or effluent > 9.0):
                has_alert = True
            elif param_name in ['bod', 'cod'] and effluent > 30.0:
                has_alert = True
            elif param_name == 'tss' and effluent > 50.0:
                has_alert = True
            elif param_name == 'turbidity' and effluent > 5.0:
                has_alert = True
            elif param_name in ['chlorine', 'cl2'] and (effluent < 0.2 or effluent > 4.0):
                has_alert = True
        
        if tlog.alert != has_alert:
            tlog.alert = has_alert
            tlog.save()


