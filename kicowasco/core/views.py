# --- MONTHLY SUMMARY VIEW WITH CSV EXPORT ---

class MonthlySummaryView(APIView):
    """
    API endpoint for monthly dashboard summary with CSV export capability.
    
    Endpoints:
    - GET /api/summaries/monthly/?year=2024&month=3 - Get JSON summary
    - GET /api/summaries/monthly/?year=2024&month=3&export=csv - Download CSV report
    """
    permission_classes = [IsAuthenticated]

    def get(self, request):
        now = datetime.datetime.now()
        try:
            year = int(request.query_params.get('year', now.year))
            month = int(request.query_params.get('month', now.month))
        except ValueError:
            year, month = now.year, now.month

        # Define date range for the specified month
        start_date = datetime.date(year, month, 1)
        if month == 12:
            end_date = datetime.date(year + 1, 1, 1) - datetime.timedelta(days=1)
        else:
            end_date = datetime.date(year, month + 1, 1) - datetime.timedelta(days=1)

        # --- DATA AGGREGATION QUERIES ---
        
        # Collection System Data
        incidents = Incident.objects.filter(
            reported_at__date__gte=start_date,
            reported_at__date__lte=end_date
        )
        
        repairs = Repair.objects.filter(
            created_at__date__gte=start_date,
            created_at__date__lte=end_date
        )
        
        # Sludge Management Data
        sludge_collections = SludgeCollection.objects.filter(
            collection_date__gte=start_date,
            collection_date__lte=end_date,
            manifest_status='completed'
        )
        
        total_sludge = sludge_collections.aggregate(Sum('volume_m3'))['volume_m3__sum'] or 0
        
        # Sludge by source type
        res_sludge = sludge_collections.filter(source_type='residential').aggregate(Sum('volume_m3'))['volume_m3__sum'] or 0
        inst_sludge = sludge_collections.filter(source_type='institutional').aggregate(Sum('volume_m3'))['volume_m3__sum'] or 0
        comm_sludge = sludge_collections.filter(source_type__in=['commercial', 'industrial']).aggregate(Sum('volume_m3'))['volume_m3__sum'] or 0
        
        # Treatment Plant Performance
        treatment_logs = TreatmentLog.objects.filter(
            report_date__gte=start_date,
            report_date__lte=end_date
        )
        
        # Calculate average BOD removal for the period
        bod_params = TreatmentParameter.objects.filter(
            tlog__in=treatment_logs,
            parameter__iexact='bod'
        ).exclude(influent_value__isnull=True).exclude(effluent_value__isnull=True)
        
        total_removal = 0
        count = 0
        for param in bod_params:
            if param.influent_value > 0:  # Avoid division by zero
                removal = ((param.influent_value - param.effluent_value) / param.influent_value) * 100
                total_removal += removal
                count += 1
        
        avg_bod_removal = total_removal / count if count > 0 else 0

        # Determine if the user wants a CSV
        export_format = request.query_params.get('export', 'json').lower()

        if export_format == 'csv':
            # Create the HttpResponse object with the appropriate CSV header.
            response = HttpResponse(
                content_type='text/csv',
                headers={'Content-Disposition': f'attachment; filename="KICOWASCO_Summary_{year}_{month:02d}.csv"'},
            )
            
            writer = csv.writer(response)
            
            # Write CSV header and data
            writer.writerow(['KICOWASCO Monthly Summary Report'])
            writer.writerow(['Period', f'{year}-{month:02d}'])
            writer.writerow(['Generated On', now.strftime('%Y-%m-%d %H:%M:%S')])
            writer.writerow(['Generated By', request.user.get_full_name() or request.user.username])
            writer.writerow([])  # Blank row
            
            # Collection System
            writer.writerow(['--- COLLECTION SYSTEM ---'])
            writer.writerow(['Metric', 'Value'])
            writer.writerow(['Total Incidents Reported', incidents.count()])
            writer.writerow(['Resolved/Closed Incidents', incidents.filter(status__in=['resolved', 'closed']).count()])
            writer.writerow(['Pending Incidents', incidents.exclude(status__in=['resolved', 'closed']).count()])
            writer.writerow(['Repairs Completed', repairs.count()])
            
            # Incident breakdown by status
            for status_code, status_label in Incident.INCIDENT_STATUS:
                count = incidents.filter(status=status_code).count()
                if count > 0:
                    writer.writerow([f'Incidents - {status_label}', count])
            writer.writerow([])
            
            # Sludge Management
            writer.writerow(['--- SLUDGE MANAGEMENT ---'])
            writer.writerow(['Source Type', 'Volume (m³)'])
            writer.writerow(['Residential', round(res_sludge, 2)])
            writer.writerow(['Institutional', round(inst_sludge, 2)])
            writer.writerow(['Commercial/Industrial', round(comm_sludge, 2)])
            writer.writerow(['Total Sludge Collected', round(total_sludge, 2)])
            writer.writerow(['Number of Collections', sludge_collections.count()])
            if sludge_collections.count() > 0:
                writer.writerow(['Average Volume per Collection', round(total_sludge / sludge_collections.count(), 2)])
            writer.writerow([])
            
            # Treatment Plant
            writer.writerow(['--- TREATMENT PLANT PERFORMANCE ---'])
            writer.writerow(['Parameter', 'Value'])
            writer.writerow(['BOD Removal (%)', round(avg_bod_removal, 2)])
            writer.writerow(['Number of Treatment Logs', treatment_logs.count()])
            writer.writerow(['Logs with Alerts', treatment_logs.filter(alert=True).count()])
            if treatment_logs.count() > 0:
                writer.writerow(['Alert Percentage', round((treatment_logs.filter(alert=True).count() / treatment_logs.count() * 100), 2)])

            return response

        # Otherwise, return the standard JSON response for the React dashboard
        data = {
            "period": f"{year}-{month:02d}",
            "generated_at": now.isoformat(),
            "generated_by": {
                "id": request.user.id,
                "name": request.user.get_full_name() or request.user.username,
                "role": getattr(request.user, 'role', 'unknown')
            },
            "collection": {
                "total_incidents": incidents.count(),
                "resolved_incidents": incidents.filter(status__in=['resolved', 'closed']).count(),
                "pending_incidents": incidents.exclude(status__in=['resolved', 'closed']).count(),
                "repairs_completed": repairs.count(),
                "incident_breakdown": {
                    status: incidents.filter(status=status).count() 
                    for status, _ in Incident.INCIDENT_STATUS
                }
            },
            "sludge": {
                "total_volume_m3": round(total_sludge, 2),
                "total_collections": sludge_collections.count(),
                "breakdown": {
                    "residential": round(res_sludge, 2),
                    "institutional": round(inst_sludge, 2),
                    "commercial": round(comm_sludge, 2)
                },
                "average_volume_per_collection": round(total_sludge / sludge_collections.count(), 2) if sludge_collections.count() > 0 else 0
            },
            "treatment": {
                "avg_bod_removal_percent": round(avg_bod_removal, 2),
                "total_logs": treatment_logs.count(),
                "logs_with_alerts": treatment_logs.filter(alert=True).count(),
                "alert_percentage": round((treatment_logs.filter(alert=True).count() / treatment_logs.count() * 100) if treatment_logs.count() > 0 else 0, 2)
            }
        }
        return Response(data)


