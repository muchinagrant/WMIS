class SludgeCollectionViewSet(viewsets.ModelViewSet):
    """
    ViewSet for the Sludge Manifest (Chain of Custody).
    """
    queryset = SludgeCollection.objects.all().order_by('-collection_date', '-created_at')
    serializer_class = SludgeCollectionSerializer
    permission_classes = [IsAuthenticated]

    def get_queryset(self):
        """Filter sludge collections based on query parameters"""
        queryset = super().get_queryset()
        user = self.request.user
        
        # Filter by driver if user is driver
        if user.role == 'driver':
            queryset = queryset.filter(exhauster_driver=user)
        
        # Filter by receiving officer if user is operator/supervisor
        if user.role in ['operator', 'supervisor'] and self.request.query_params.get('my_receipts'):
            queryset = queryset.filter(receiving_officer=user)
        
        # Filter by date range
        from_date = self.request.query_params.get('from_date', None)
        to_date = self.request.query_params.get('to_date', None)
        
        if from_date:
            queryset = queryset.filter(collection_date__gte=from_date)
        if to_date:
            queryset = queryset.filter(collection_date__lte=to_date)
        
        # Filter by source type
        source_type = self.request.query_params.get('source_type', None)
        if source_type:
            queryset = queryset.filter(source_type=source_type)
        
        # Filter by area/ward
        area_ward = self.request.query_params.get('area_ward', None)
        if area_ward:
            queryset = queryset.filter(area_ward__icontains=area_ward)
        
        # Filter by manifest status
        manifest_status = self.request.query_params.get('manifest_status', None)
        if manifest_status:
            queryset = queryset.filter(manifest_status=manifest_status)
        
        # Filter by exhauster
        exhauster_id = self.request.query_params.get('exhauster_id', None)
        if exhauster_id:
            queryset = queryset.filter(exhauster_id=exhauster_id)
            
        return queryset

    def perform_create(self, serializer):
        serializer.save(exhauster_driver=self.request.user)

    @action(detail=False, methods=['get'])
    def pending(self, request):
        """Get pending manifests (not yet completed)"""
        pending = self.get_queryset().filter(manifest_status='pending')
        serializer = self.get_serializer(pending, many=True)
        return Response(serializer.data)

    @action(detail=False, methods=['get'])
    def completed(self, request):
        """Get completed manifests"""
        completed = self.get_queryset().filter(manifest_status='completed')
        serializer = self.get_serializer(completed, many=True)
        return Response(serializer.data)

    @action(detail=True, methods=['post'])
    def complete(self, request, pk=None):
        """Mark a sludge collection manifest as completed with receiving officer notes"""
        collection = self.get_object()
        
        if collection.manifest_status == 'completed':
            return Response(
                {'error': 'This manifest is already completed'},
                status=status.HTTP_400_BAD_REQUEST
            )
        
        collection.receiving_officer = request.user
        collection.receiving_notes = request.data.get('receiving_notes', '')
        collection.manifest_status = 'completed'
        collection.save()
        
        serializer = self.get_serializer(collection)
        return Response(serializer.data)

    @action(detail=False, methods=['get'])
    def my_collections(self, request):
        """Get collections where current user was the driver"""
        collections = self.get_queryset().filter(exhauster_driver=request.user)
        serializer = self.get_serializer(collections, many=True)
        return Response(serializer.data)

    @action(detail=False, methods=['get'])
    def received_by_me(self, request):
        """Get collections received by current user"""
        collections = self.get_queryset().filter(receiving_officer=request.user)
        serializer = self.get_serializer(collections, many=True)
        return Response(serializer.data)

    @action(detail=False, methods=['get'])
    def summary(self, request):
        """Get summary statistics for sludge collections"""
        queryset = self.get_queryset()
        total = queryset.count()
        pending = queryset.filter(manifest_status='pending').count()
        completed = queryset.filter(manifest_status='completed').count()
        
        total_volume = queryset.aggregate(Sum('volume_m3'))['volume_m3__sum'] or 0
        avg_volume = queryset.aggregate(Avg('volume_m3'))['volume_m3__avg'] or 0
        
        source_breakdown = {}
        for source_type, _ in SludgeCollection.SOURCE_CHOICES:
            count = queryset.filter(source_type=source_type).count()
            if count > 0:
                source_breakdown[source_type] = count
        
        thirty_days_ago = timezone.now().date() - datetime.timedelta(days=30)
        recent = queryset.filter(collection_date__gte=thirty_days_ago).count()
        
        return Response({
            'total_manifests': total,
            'pending': pending,
            'completed': completed,
            'completion_rate': round((completed / total * 100) if total > 0 else 0, 2),
            'total_volume_m3': round(total_volume, 2),
            'average_volume_m3': round(avg_volume, 2),
            'by_source_type': source_breakdown,
            'recent_manifests_30d': recent,
        })


