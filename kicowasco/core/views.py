class IncidentViewSet(viewsets.ModelViewSet):
    """
    ViewSet for managing incidents.
    """
    queryset = Incident.objects.all().order_by('-reported_at', '-created_at')
    serializer_class = IncidentSerializer
    permission_classes = [IsAuthenticated]

    def get_queryset(self):
        """Filter incidents based on query params"""
        queryset = super().get_queryset()
        
        status = self.request.query_params.get('status', None)
        if status:
            queryset = queryset.filter(status=status)
        
        assigned_to = self.request.query_params.get('assigned_to', None)
        if assigned_to:
            queryset = queryset.filter(assigned_to_id=assigned_to)
        
        from_date = self.request.query_params.get('from_date', None)
        to_date = self.request.query_params.get('to_date', None)
        
        if from_date:
            queryset = queryset.filter(reported_at__date__gte=from_date)
        if to_date:
            queryset = queryset.filter(reported_at__date__lte=to_date)
            
        return queryset

    def perform_create(self, serializer):
        serializer.save(created_by=self.request.user)
    
    @action(detail=True, methods=['post'])
    def assign(self, request, pk=None):
        """Assign an incident to a user"""
        incident = self.get_object()
        user_id = request.data.get('user_id')
        
        if not user_id:
            return Response(
                {'error': 'user_id is required'},
                status=status.HTTP_400_BAD_REQUEST
            )
        
        try:
            user = User.objects.get(id=user_id)
            incident.assigned_to = user
            incident.save()
            serializer = self.get_serializer(incident)
            return Response(serializer.data)
        except User.DoesNotExist:
            return Response(
                {'error': 'User not found'},
                status=status.HTTP_404_NOT_FOUND
            )
    
    @action(detail=True, methods=['post'])
    def update_status(self, request, pk=None):
        """Update incident status"""
        incident = self.get_object()
        new_status = request.data.get('status')
        
        if not new_status:
            return Response(
                {'error': 'status is required'},
                status=status.HTTP_400_BAD_REQUEST
            )
        
        incident.status = new_status
        incident.save()
        serializer = self.get_serializer(incident)
        return Response(serializer.data)
    
    @action(detail=False, methods=['get'])
    def summary(self, request):
        """Get summary statistics for incidents"""
        queryset = self.get_queryset()
        total = queryset.count()
        by_status = {}
        
        for status_code, status_label in Incident.INCIDENT_STATUS:
            count = queryset.filter(status=status_code).count()
            if count > 0:
                by_status[status_code] = count
        
        return Response({
            'total': total,
            'by_status': by_status,
        })