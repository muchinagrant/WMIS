class RepairViewSet(viewsets.ModelViewSet):
    """
    ViewSet for managing repair completion certificates.
    """
    queryset = Repair.objects.all().order_by('-created_at')
    serializer_class = RepairSerializer
    permission_classes = [IsAuthenticated]

    def get_queryset(self):
        """Filter repairs based on user role and query params"""
        queryset = super().get_queryset()
        user = self.request.user
        
        if user.role == 'technician':
            queryset = queryset.filter(technician=user)
        
        incident_id = self.request.query_params.get('incident', None)
        if incident_id:
            queryset = queryset.filter(incident_id=incident_id)
        
        certified = self.request.query_params.get('certified', None)
        if certified is not None:
            if certified.lower() == 'true':
                queryset = queryset.filter(certified_at__isnull=False)
            elif certified.lower() == 'false':
                queryset = queryset.filter(certified_at__isnull=True)
                
        return queryset

    def perform_create(self, serializer):
        serializer.save(technician=self.request.user)

    @action(detail=True, methods=['patch'], permission_classes=[IsAuthenticated, IsSupervisor])
    def certify(self, request, pk=None):
        """
        Certify a repair with supervisor signature.
        """
        repair = self.get_object()
        
        if repair.certified_at:
            return Response(
                {'error': 'This repair has already been certified.'},
                status=status.HTTP_400_BAD_REQUEST
            )
        
        signature = request.FILES.get('supervisor_signature')
        
        if not signature:
            return Response(
                {'error': 'Supervisor signature is required to certify a repair.'}, 
                status=status.HTTP_400_BAD_REQUEST
            )
            
        repair.supervisor = request.user
        repair.supervisor_signature = signature
        repair.certified_at = timezone.now()
        repair.save()
        
        serializer = self.get_serializer(repair)
        return Response(serializer.data)
    
    @action(detail=False, methods=['get'])
    def uncertified(self, request):
        """Get all repairs that haven't been certified yet"""
        uncertified = self.get_queryset().filter(certified_at__isnull=True)
        serializer = self.get_serializer(uncertified, many=True)
        return Response(serializer.data)
    
    @action(detail=False, methods=['get'])
    def my_repairs(self, request):
        """Get repairs created by or assigned to the current user"""
        user = request.user
        repairs = self.get_queryset().filter(
            Q(technician=user) | Q(supervisor=user)
        ).distinct()
        serializer = self.get_serializer(repairs, many=True)
        return Response(serializer.data)


